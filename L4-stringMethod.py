intro="   Hello Everyone This Side Mayur Here  !! "
# lowerCase()
print("Upper_case: ",intro.upper())
# upperCase()
print("Lower_case:",intro.lower())
# split method
print("Split with commas:",intro.split(","))
print("Split without space:",intro.split(" "))
print("Intro: ",intro)
# len ():
print("Length_of_Intro_string:",len(intro))
# strip():
print("Remove_whiteSpace_from_beginning_or_end_using_strip():",intro.strip())
print("length_of_Intro_str_using_using_strip_method:",len(intro.strip()))
# Replace method
print("replace mayur with bhagwatiShiva using replace():",intro.replace("Mayur","bhagwatiShiva"))
print("Intro: ",intro)
print("replace_e_with_m_using_replace():",intro.replace("e","m"))

